    /// </summary>
    G = 7,

    /// <summary>
    /// Half-stepped G (Sol) note.
    /// </summary>
    GSharp = 8,

    /// <summary>
    /// A (La) note.
    /// </summary>
    A = 9,

    /// <summary>
    /// Half-stepped A (La) note.
    /// </summary>
    ASharp = 10,

    /// <summary>
    /// B (Si) note.
    /// </summary>
